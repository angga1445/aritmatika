public class Aritmatika {
    
    public static void main(String[] args) {
        //deklarasi variable
        int a=10;
        int b=5;
        double tambah,bagi,kali,kurang;
        
        //rumus aritmatika
        tambah= a+b;
        kurang= a-b;
        kali= a*b;
        bagi= a/b;
        
        //output nilai
        System.out.println("Nilai penjumlahan: "+tambah+"\n"+"Nilai pengurangan: "+kurang+"\n"+"Nilai perkalian: "+kali+"\n"+"Nilai pembagian: "+bagi);
    }
    
}
